    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
                                          
    String username = UsernameField.getText().trim();
    String password = String.valueOf(PasswordField.getPassword()).trim();

    PreparedStatement ps;
    ResultSet rs;

    // ===== Admin Login =====
    String adminQuery = "SELECT * FROM `users` WHERE `username`=? AND password=?";
    try {
        ps = DB.getConnection().prepareStatement(adminQuery);
        ps.setString(1, username);
        ps.setString(2, password);
        rs = ps.executeQuery();

        if(rs.next()) { // Admin detected
            System.out.println("Admin Login");

            // Ask for admin code
            String storedCode = rs.getString("code"); // fetch from DB
            String inputCode = JOptionPane.showInputDialog(this, "Enter your admin code:", "Admin Verification", JOptionPane.PLAIN_MESSAGE);

            if(inputCode != null && inputCode.equals(storedCode)) {
                CustomPopup.showInfo(this, "Login Successful!", "Success");
                new Admin_DashBoard().setVisible(true);
                this.dispose();
            } else {
                CustomPopup.showError(this, "Incorrect admin code! Access denied.", "Error");
            }

        } else {
            // ===== Customer Login =====
            // ===== Customer Login =====
String customerQuery = "SELECT * FROM `customer` WHERE `username`=?";
try {
    ps = DB.getConnection().prepareStatement(customerQuery);
    ps.setString(1, username);
    rs = ps.executeQuery();

    if(rs.next()) {
        String storedHash = rs.getString("password"); // hashed password from DB

        if(BCrypt.checkpw(password, storedHash)) {
            // Password matches
            System.out.println("Customer Login");

            // Get profile picture as byte array
            byte[] profilePic = rs.getBytes("profile_picture");

            // Check if user already exists in logged_in_user
            String checkLoggedIn = "SELECT * FROM `logged_in_user` WHERE username=?";
            PreparedStatement psCheck = DB.getConnection().prepareStatement(checkLoggedIn);
            psCheck.setString(1, username);
            ResultSet rsCheck = psCheck.executeQuery();

            if(!rsCheck.next()) {
                // Insert into logged_in_user
                String insertLoggedIn = "INSERT INTO logged_in_user (username, profile_picture) VALUES (?, ?)";
                PreparedStatement psInsert = DB.getConnection().prepareStatement(insertLoggedIn);
                psInsert.setString(1, username);
                psInsert.setBytes(2, profilePic);
                psInsert.executeUpdate();
            }

            // Proceed to customer dashboard
            JOptionPane.showMessageDialog(this, "Customer Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
            String enteredUsername = UsernameField.getText().trim(); // username from login
new Primary_Screen_Logged(enteredUsername).setVisible(true);
this.dispose();


        } else {
            // Wrong password
            JOptionPane.showMessageDialog(this, "Invalid Username or Password!", "Error", JOptionPane.INFORMATION_MESSAGE);
            new Form_Login().setVisible(true);
            this.dispose();
        }
    } else {
        // Username not found
        JOptionPane.showMessageDialog(this, "Invalid Username or Password!", "Error", JOptionPane.INFORMATION_MESSAGE);
        new Form_Login().setVisible(true);
        this.dispose();
    }

} catch(SQLException ex) {
    System.out.println("Customer DB Error: " + ex.getMessage());
    JOptionPane.showMessageDialog(this, "Customer DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}

        }

    } catch(SQLException ex) {
        System.out.println("Admin DB Error: " + ex.getMessage());
        JOptionPane.showMessageDialog(this, "Admin DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }







    }//GEN-LAST:event_LoginButtonActionPerformed

